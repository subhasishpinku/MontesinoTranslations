    public static Bitmap getBitmapFromContentUri(Context context, Uri contentUri) {
        try {
            ContentResolver resolver = context.getContentResolver();
            InputStream inputStream = resolver.openInputStream(contentUri);
            Bitmap bitmap = BitmapFactory.decodeStream(inputStream);
            Bitmap rotate_Bitmap = rotateImageIfRequired(context, bitmap, contentUri);
            if (inputStream != null) {
                inputStream.close();
            }
            return rotate_Bitmap;
        } catch (IOException e) {
            e.printStackTrace();
            return null;
        }
    }

